# função para calcular a potência de dois números
def calcula_potencia(x, y):
    return x ** y


# função para transformar o texto em maiusculas
def maiuscula(x):
    return x.upper()


# chama a função para calcular 2 elevado a 3
print(calcula_potencia(2, 3))

# chama a função para colocar o texto 'oi' em maiúscula 'OI'
print(maiuscula("oi"))
